'''
Arithmetic operators are used with numeric values to perform common mathematical operations:
'''


num1 = int(input("Insert first number: "))
num2 = int(input("Input second number: "))

#Will add num 1 and num2
print(num1 + num2) 

#Will subtract num 1 and num2
print(num1 - num2)

#Will multiply num 1 and num2
print(num1 * num2)

#Will divide num 1 and num2
print(num1 / num2)

#Will get the remainder between num1 and num2
print(num1 % num2)

#Will raise the num1 to the power of num2 (Exponentiation) (2^3)
print(num1 ** num2)

#divides num 1 and num 2 and rounds down to the nearest whole number 
# Example: 2 / 3 = 0.6666 -> round down to  0
print(num1 // num2)
