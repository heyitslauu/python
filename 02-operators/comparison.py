'''
Comparison Operators are used to compare 2 values.
It always return boolean values: True or False

The comparison operators are:

== for equal
!= for NOT equal
> for greater than
< for less than
>= for greater than OR equal
<= for less than OR equal
'''


num1 = 5
num2 = 10

print(num1 == num2) #returns False
print(num1 != num2) #returns True
print(num1 > num2) #returns False
print(num1 < num2) #returns True
print(num1 >= num2) #returns False
print(num1 <= num2) #returns True
